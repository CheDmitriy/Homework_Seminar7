{
  double[,] newArray = new double[rows,columns];
  Random random = new Random();
  {
    for(int i = 0; i < rows; i++)
       for(int j = 0; j < columns; j++)
          newArray[i,j] = random.NextDouble() * 10;
  }
  return newArray;
}

